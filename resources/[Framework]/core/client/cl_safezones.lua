    for _, zone in pairs(safezones) do
        -- print("Creating safezone: " .. zone.name)  -- Debug print

        local success = exports["core"]:AddPolyZone(
            zone.name,
            zone.points,
            {
                debugPoly = true,
                minZ = zone.options.minZ,
                maxZ = zone.options.maxZ,
