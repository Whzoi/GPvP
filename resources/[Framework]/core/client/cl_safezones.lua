local safezoneInDelay = 0
local safezoneCooldownEndTime = 0
local isInSafezoneDuringCooldown = false

exports("safezoneDelay", function(delay)
    if delay then
        safezoneInDelay = 2000
    else
        safezoneInDelay = 0
    end
end)

-- Thread to check if player is still in safezone when cooldown finishes
CreateThread(function()
    while true do
        Citizen.Wait(100)
        
        if isInSafezoneDuringCooldown then
            local currentTime = GetGameTimer()
            if currentTime >= safezoneCooldownEndTime then
                -- Cooldown finished, check if still in safezone
                if inSafeZone then
                    -- Still in safezone, make them passive/ghost
                    NetworkSetFriendlyFireOption(false)
                    SetCanAttackFriendly(PlayerPedId(), false, false)
                    SetLocalPlayerAsGhost(true)
                    NetworkSetPlayerIsPassive(true)
                end
                isInSafezoneDuringCooldown = false
            end
        end
    end
end)

AddEventHandler("polyzone:enter", function(name)
    if string.match(name, 'safezone') then
        Citizen.Wait(safezoneInDelay)
        inSafeZone = true
        
        local currentTime = GetGameTimer()
        if currentTime < safezoneCooldownEndTime then
            -- Still in cooldown period, don't make them passive/ghost yet
            isInSafezoneDuringCooldown = true
            -- Keep them killable
            NetworkSetFriendlyFireOption(true)
            SetCanAttackFriendly(PlayerPedId(), true, true)
            SetLocalPlayerAsGhost(false)
            NetworkSetPlayerIsPassive(false)
        else
            -- Cooldown finished or never started, make them passive/ghost immediately
            NetworkSetFriendlyFireOption(false)
            SetCanAttackFriendly(PlayerPedId(), false, false)
            SetLocalPlayerAsGhost(true)
            NetworkSetPlayerIsPassive(true)
            isInSafezoneDuringCooldown = false
        end

        -- print("Entered safezone: " .. name)  -- Debug print
    end
end)

AddEventHandler("polyzone:exit", function(name)
    if string.match(name, 'safezone') then
        inSafeZone = false
        isInSafezoneDuringCooldown = false
        
        -- Start 10 second cooldown
        safezoneCooldownEndTime = GetGameTimer() + 10000
        
        SetLocalPlayerAsGhost(false)
        NetworkSetPlayerIsPassive(false)
        NetworkSetFriendlyFireOption(true)
        SetCanAttackFriendly(PlayerPedId(), true, true)

        -- print("Exited safezone: " .. name)  -- Debug print
    end
end)

exports("putInSafeZone", function(value)
    inSafeZone = value
    SetLocalPlayerAsGhost(value)
    NetworkSetPlayerIsPassive(value)
    NetworkSetFriendlyFireOption(not value)
    SetCanAttackFriendly(PlayerPedId(), not value, not value)

    -- print("inSafeZone is now: " .. tostring(inSafeZone))  -- Debug print
end)

-- When killed during or outside cooldown, immediately reset cooldown and go passive if applicable
AddEventHandler('baseevents:onPlayerKilled', function(killerId, deathData)
    -- Reset cooldown timer
    safezoneCooldownEndTime = GetGameTimer() -- zero it out
    isInSafezoneDuringCooldown = false

    -- If the player is within any safezone, immediately re-apply passive mode
    if inSafeZone then
        NetworkSetFriendlyFireOption(false)
        SetCanAttackFriendly(PlayerPedId(), false, false)
        SetLocalPlayerAsGhost(true)
        NetworkSetPlayerIsPassive(true)
    end
end)



exports("inSafeZone", function()
    -- print("Checking inSafeZone status")  -- Debug print
    return inSafeZone
end)


