local myClonedPed = nil
local myCamera = nil

-- Function to create a cloned ped at a specified location
local function createPed(position, model)
    local playerPed = PlayerPedId()
    myClonedPed = ClonePed(PlayerPedId(), false, false, true)
  
    -- Set the ped at the specified coordinates and heading
    SetEntityCoords(myClonedPed, position, false, false, false, true)
    SetEntityHeading(myClonedPed, spawnHeading)
    FreezeEntityPosition(myClonedPed, true)
    TaskSetBlockingOfNonTemporaryEvents(myClonedPed, true)
  
    return myClonedPed
end

-- Function to create and set up a camera looking at the cloned ped
local function createCamera(targetPed)
    local pedCoords = GetEntityCoords(targetPed)
    local heading = GetEntityHeading(targetPed)
    
    -- Calculate the offset to position the camera in front of the ped
    local camOffset = vector3(0.0, 3.0, 0.5) -- Adjust as needed, 2 units forward, 0 units sideways, 0.5 units up

    -- Calculate the camera position based on the ped's heading
    local camX = pedCoords.x + camOffset.x * math.cos(math.rad(heading)) - camOffset.y * math.sin(math.rad(heading))
    local camY = pedCoords.y + camOffset.x * math.sin(math.rad(heading)) + camOffset.y * math.cos(math.rad(heading))
    local camZ = pedCoords.z + camOffset.z

    -- Set the camera rotation to face the ped
    local camRot = vector3(0.0, 0.0, heading + 90.0) -- Facing the opposite direction

    local cameraHandle = CreateCam("DEFAULT_SCRIPTED_CAMERA", true)
    SetCamCoord(cameraHandle, camX, camY, camZ)
    SetCamRot(cameraHandle, camRot.x, camRot.y, camRot.z) -- Set individual components of the rotation vector
    SetCamFov(cameraHandle, 50.0)
    PointCamAtEntity(cameraHandle, targetPed)
    RenderScriptCams(true, false, 0, true, true)

    -- Set focus to the locker area
    SetFocusArea(spawnCoords.x, spawnCoords.y, spawnCoords.z, 0.0, 0.0, 0.0)

    myCamera = cameraHandle
  
    return myCamera
end

local function destroyCamera(cameraHandle)
    if cameraHandle then
        DestroyCam(cameraHandle)
        RenderScriptCams(false, false, 0, true, true)
        cameraHandle = nil
    end
end

