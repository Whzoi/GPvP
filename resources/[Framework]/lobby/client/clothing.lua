function SaveClothingData()
local playerPed = PlayerPedId()
local clothingData = {}

for i = 0, 11 do
    local currentComponent = GetPedDrawableVariation(playerPed, i)
    local currentTexture = GetPedTextureVariation(playerPed, i)
    clothingData["component_" .. i] = {
        item_id = currentComponent,
        texture = currentTexture
    }
end

for i = 0, 7 do
    local currentProp = GetPedPropIndex(playerPed, i)
    local currentPropTexture = GetPedPropTextureIndex(playerPed, i)
    clothingData["prop_" .. i] = {
        item_id = currentProp,
        texture = currentPropTexture
    }
end

local clothingDataJson = json.encode(clothingData)
SetResourceKvp('clothingData_' .. PlayerId(), clothingDataJson)
end

function LoadClothingData()
local playerPed = PlayerPedId()
local clothingDataJson = GetResourceKvpString('clothingData_' .. PlayerId())

if clothingDataJson then
    local clothingData = json.decode(clothingDataJson)
    
    for i = 0, 11 do
        local componentData = clothingData["component_" .. i]
        if componentData then
            SetPedComponentVariation(playerPed, i, componentData.item_id, componentData.texture, 0)
        end
    end

    for i = 0, 7 do
        local propData = clothingData["prop_" .. i]
        if propData then
            SetPedPropIndex(playerPed, i, propData.item_id, propData.texture, true)
        end
    end

else
    -- print("No saved clothing data found. Applying default clothing.")
    ApplyDefaultClothing()
end
end


function SetClothingByName(category, itemName)
    local playerPed = PlayerPedId()
    local categoryTable = clothingTable[category]

    if categoryTable then
        for _, clothingItem in pairs(categoryTable) do
            if clothingItem.name == itemName then
                -- Apply each component change for the item
                for _, component in pairs(clothingItem.components or {}) do
                    SetPedComponentVariation(playerPed, component.component, component.item_id, component.texture, component.palette)
                end
                -- Apply each prop change for the item
                for _, prop in pairs(clothingItem.props or {}) do
                    SetPedPropIndex(playerPed, prop.prop, prop.item_id, prop.texture, true)
                end
                
                SaveClothingData()

                -- print("Clothing set: ".. category.. " - ".. itemName)
                return
            end
        end
        print("Item not found in category: " .. category)
    else
        print("Invalid clothing category: " .. category)
    end
end

