    for k,v in pairs(kit) do API.GiveItem(v.item, v.amount, v.slot) end 
    local primary = API.FindPrimaryWeapon(kit)
    local ammoCount = API.GetKitAmmo(kit)
    GiveWeaponToPed(Player.Ped(), GetHashKey(primary), 0, false, true)
