    savedKits[Player.Ped()] = {
        genre = Player.CurrentKitGenre,
        kit = Player.CurrentKit
    }
end

-- Function to get the saved kit
function GetSavedKit()
    return savedKits[Player.Ped()]
end

exports("GetSavedKit", GetSavedKit)

