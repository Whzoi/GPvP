function API.ClearItems()
    RemoveAllPedWeapons(Player.Ped())
    TriggerServerEvent("zbrp:Player:clearItems")
end

function DoKitStuff(genre, kit)
    local isGenreAllowed = true
    if Kits[genre] and Kits[genre][kit] then
        -- Check if the genre and kit exist in your predefined data
        isGenreAllowed = true
    else
        isGenreAllowed = false
        TriggerEvent('chat:addMessage', {
            template = '<div class="chat-message"><b2>{0}:</b2> <b1>{1}</b1></div>',
            args = { "[SYSTEM]", "This kit is not available." }
        })
        return
    end

    Player.CurrentKitGenre = genre
    Player.CurrentKit = kit
    API.ClearItems()
    API.GiveKit()
    SaveCurrentKit() -- Save the current kit after giving it
end

exports("DoKitStuff", DoKitStuff)

-- Function to save the current kit
